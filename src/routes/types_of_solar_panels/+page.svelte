<h4>Types Of Solar Panels</h4>

<div>
	Monocrystalline and Polycrystalline are the 2 common types of solar panels.
</div>

<div>
	Polycrystalline panels tend to be slightly cheaper, but take more space,
	than Monocrystalline panels. For example if you are building a camper van a
	Monocrystalline is better because you have limited space on a van roof. If
	space is a non issue, like building a solar array in a field, it typically
	doesn't really matter which panel type is used.
</div>
